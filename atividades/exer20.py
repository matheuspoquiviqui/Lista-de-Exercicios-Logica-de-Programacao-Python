# Peça o salário de um funcionário e calcule um aumento de 15%.

print("-- CÁLCULO DE AUMENTO --")
salario = float(input("Informe o seu salário atual: R$ "))

aumento = salario * 0.15
totalComAumento = (salario+aumento)

print("Você terá um aumento de 15% = R$", aumento)
print("Seu salário com o aumento será = R$", totalComAumento)

